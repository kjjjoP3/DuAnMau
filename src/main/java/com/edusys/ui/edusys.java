    void openDoanhThu(int index) {
        if (Auth.isLogin()) {
            if (index == 3 && !Auth.isManager()) {
                MgsBox.Alert(this, "Bạn không có quyền xem doanh thu");
                thongkepanel.setDelete();
            } else if (index != 3 && !Auth.isManager()) {
                if (thongkepanel == null) {
                    thongkepanel = new ThongKeJPanel();
                    ImageIcon icon = new ImageIcon(getClass().getResource("/images/icons8-chart-24.png"));
                    tblMainBoard.addTab("Quản lý thống kê", icon, thongkepanel, "Quản lý thống kê");
                    tk.setSize(60, 30);
                    tk.setHorizontalAlignment(JButton.CENTER);
                    Border bd = BorderFactory
                            .createEtchedBorder(EtchedBorder.RAISED);
                    tk.setBorder(bd);
                    tk.setLocation(1450, 5);

                    ImageIcon ic = new ImageIcon(getClass().getResource("/images/icons8-close-32.png"));
                    tk.setIcon(ic);
                    tk.addActionListener(new ActionListener() {
                        @Override
                        public void actionPerformed(ActionEvent e) {
                            closeDT();
                        }

                    });
                    thongkepanel.add(tk);
                }
                thongkepanel.setDelete();
                tblMainBoard.setSelectedComponent(thongkepanel);
                thongkepanel.selectTab(index);

            } else if (Auth.isManager()) {
                if (thongkepanel == null) {
                    thongkepanel = new ThongKeJPanel();
                    ImageIcon icon = new ImageIcon(getClass().getResource("/images/icons8-chart-24.png"));
                    tblMainBoard.addTab("Quản lý thống kê", icon, thongkepanel, "Quản lý thống kê");
                    tk.setSize(60, 30);
                    tk.setHorizontalAlignment(JButton.CENTER);
                    Border bd = BorderFactory
                            .createEtchedBorder(EtchedBorder.RAISED);
                    tk.setBorder(bd);
                    tk.setLocation(1450, 5);

                    ImageIcon ic = new ImageIcon(getClass().getResource("/images/icons8-close-32.png"));
                    tk.setIcon(ic);
                    tk.addActionListener(new ActionListener() {
                        @Override
                        public void actionPerformed(ActionEvent e) {
                            closeDT();
                        }

                    });
                    thongkepanel.add(tk);
                }

                tblMainBoard.setSelectedComponent(thongkepanel);
                thongkepanel.selectTab(index);
            }

        } else {
            MgsBox.Alert(this, "Vui lòng đăng nhập");
        }
    }

    void closeDT() {
        tblMainBoard.remove(thongkepanel);
        thongkepanel = null;
    }

