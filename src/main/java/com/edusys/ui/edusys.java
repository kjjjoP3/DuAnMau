    private void dangXuat() {
        if (JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn đăng xuất ?", "Confirmation", JOptionPane.YES_NO_OPTION) == JOptionPane.NO_OPTION) {
            return;
        }

        tblMainBoard.remove(nhanvienpanel);
        tblMainBoard.remove(chuyendepanel);
        tblMainBoard.remove(khoahocpanel);
        tblMainBoard.remove(hocvienpanel);
        tblMainBoard.remove(nguoihocpanel);
        tblMainBoard.remove(thongkepanel);

        nhanvienpanel = null;
        chuyendepanel = null;
        khoahocpanel = null;
        hocvienpanel = null;
        nguoihocpanel = null;
        thongkepanel = null;
        Auth.clear();
        lblCHAO.setText("");
        new DangNhapJDialog(this, true).setVisible(true);
        dangnhapthanhcong();
    }
